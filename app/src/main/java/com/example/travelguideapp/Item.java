package com.example.travelguideapp;

public class Item {
    private int image;
    private int description;
    private int about;

    public int getImage() {
        return image;
    }

    public void setImage(int img) {
        this.image = img;
    }

    public int getDescription() {
        return description;
    }

    public void setDescription(int des) {
        this.description = des;
    }

    public void setAbout(int aBout) {
        this.about = aBout;
    }

    public int getAbout() {
        return about;
    }

}
